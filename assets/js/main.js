function createStarField() {
  const canvas = document.querySelector("#star-canvas");
  if (!canvas) return;

  const context = canvas.getContext("2d");
  const reducedMotion = matchMedia("(prefers-reduced-motion: reduce)").matches;
  let width = 0;
  let height = 0;
  let stars = [];
  let frame = 0;

  function resize() {
    const rect = canvas.getBoundingClientRect();
    const ratio = Math.min(devicePixelRatio || 1, 1.5);
    width = rect.width;
    height = rect.height;
    canvas.width = Math.floor(width * ratio);
    canvas.height = Math.floor(height * ratio);
    context.setTransform(ratio, 0, 0, ratio, 0, 0);
    stars = Array.from({ length: width < 700 ? 25 : 44 }, (_, index) => ({
      x: Math.random() * width,
      y: Math.random() * height,
      radius: Math.random() * 1.35 + 0.25,
      alpha: Math.random() * 0.65 + 0.2,
      speed: Math.random() * 0.008 + 0.002,
      phase: index
    }));
  }

  function draw() {
    context.clearRect(0, 0, width, height);

    stars.forEach((star) => {
      const pulse = reducedMotion
        ? star.alpha
        : star.alpha * (0.62 + Math.sin(frame * star.speed + star.phase) * 0.38);
      context.beginPath();
      context.fillStyle = `rgba(109, 181, 255, ${Math.max(0.08, pulse)})`;
      context.shadowColor = "rgba(85, 221, 224, .55)";
      context.shadowBlur = star.radius > 1 ? 8 : 3;
      context.arc(star.x, star.y, star.radius, 0, Math.PI * 2);
      context.fill();
    });

    context.shadowBlur = 0;
    frame += 1;
    if (!reducedMotion) requestAnimationFrame(draw);
  }

  resize();
  draw();
  addEventListener("resize", resize, { passive: true });
}

createStarField();

