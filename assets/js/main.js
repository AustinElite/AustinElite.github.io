function getInitialLanguage() {
  const saved = localStorage.getItem("austinelite-language");
  if (saved === "zh" || saved === "en") return saved;
  return navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
}

function applyLanguage(language) {
  const dictionary = translations[language];
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.documentElement.dataset.language = language;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = dictionary[element.dataset.i18n];
    if (value) element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = dictionary[element.dataset.i18nPlaceholder];
    if (value) element.setAttribute("placeholder", value);
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const value = dictionary[element.dataset.i18nAria];
    if (value) element.setAttribute("aria-label", value);
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const value = dictionary[element.dataset.i18nAlt];
    if (value) element.setAttribute("alt", value);
  });

  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const value = dictionary[element.dataset.i18nTitle];
    if (value) element.setAttribute("title", value);
  });

  document.querySelectorAll("[data-post-title-zh]").forEach((element) => {
    const value = element.dataset[language === "zh" ? "postTitleZh" : "postTitleEn"];
    if (value) element.textContent = value;
  });

  document.querySelectorAll("[data-post-description-zh]").forEach((element) => {
    const value = element.dataset[language === "zh" ? "postDescriptionZh" : "postDescriptionEn"];
    if (value) element.textContent = value;
  });

  const description = document.querySelector('meta[name="description"]');
  if (document.body.dataset.page === "home" || !document.body.dataset.page) {
    document.title = dictionary["meta.title"];
    description?.setAttribute("content", dictionary["meta.description"]);
  } else if (document.body.dataset.page === "admin") {
    document.title = dictionary["admin.meta.title"];
  }

  languageButton?.setAttribute(
    "aria-label",
    language === "zh" ? "Switch to English" : "切换到中文"
  );
  themeButton?.setAttribute(
    "aria-label",
    document.documentElement.dataset.theme === "dark"
      ? (language === "zh" ? "切换到亮色主题" : "Switch to light theme")
      : (language === "zh" ? "切换到暗色主题" : "Switch to dark theme")
  );
  menuButton?.setAttribute(
    "aria-label",
    dictionary[document.body.classList.contains("menu-open") ? "menu.close" : "menu.open"]
  );

  window.dispatchEvent(new CustomEvent("austinelite:language", { detail: language }));
}

let currentLanguage = getInitialLanguage();
applyLanguage(currentLanguage);

languageButton?.addEventListener("click", () => {
  currentLanguage = currentLanguage === "zh" ? "en" : "zh";
  localStorage.setItem("austinelite-language", currentLanguage);
  applyLanguage(currentLanguage);
});

function applyTheme(theme) {
  const isDark = theme === "dark";
  document.documentElement.dataset.theme = theme;
  themeIcon && (themeIcon.textContent = isDark ? "☀" : "☾");
  themeButton?.setAttribute(
    "aria-label",
    isDark
      ? (currentLanguage === "zh" ? "切换到亮色主题" : "Switch to light theme")
      : (currentLanguage === "zh" ? "切换到暗色主题" : "Switch to dark theme")
  );
  themeButton?.setAttribute("aria-pressed", String(isDark));
  themeMeta?.setAttribute("content", isDark ? "#090909" : "#f4f4f2");
}

applyTheme(document.documentElement.dataset.theme || "dark");

themeButton?.addEventListener("click", () => {
  const next = document.documentElement.dataset.theme === "dark" ? "light" : "dark";
  localStorage.setItem("austinelite-theme", next);
  applyTheme(next);
});

matchMedia("(prefers-color-scheme: dark)").addEventListener("change", (event) => {
  if (!localStorage.getItem("austinelite-theme")) {
    applyTheme(event.matches ? "dark" : "light");
  }
});

function setMenu(open) {
  document.body.classList.toggle("menu-open", open);
  menuButton?.setAttribute("aria-expanded", String(open));
  menuButton?.setAttribute(
    "aria-label",
    translations[currentLanguage][open ? "menu.close" : "menu.open"]
  );
}

menuButton?.addEventListener("click", () => {
  setMenu(!document.body.classList.contains("menu-open"));
});

mobileMenu?.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => setMenu(false));
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") setMenu(false);
});

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12 }
);

document.querySelectorAll(".reveal").forEach((element) => revealObserver.observe(element));

